// Helper function to get all articles
export const getAllArticles = () => {
  const allArticles = [];
  Object.keys(articlesData).forEach((categoryKey) => {
    const category = articlesData[categoryKey];
    category.articles.forEach((article) => {
      allArticles.push({
        ...article,
        category: categoryKey,
        categoryTitle: category.title,
        categoryColor: category.color,
      });
    });
  });
  return allArticles;
};

// Helper function to get article by ID
export const getArticleById = (articleId) => {
  const allArticles = getAllArticles();
  return allArticles.find((article) => article.id === articleId);
};

// Helper function to get articles by category
export const getArticlesByCategory = (categoryKey) => {
  return articlesData[categoryKey] || null;
};
